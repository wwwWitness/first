public class ChangeChar {
    public static void main(String[] args){
        //\t:制表位，实现对齐功能
        System.out.println("小学\t初中\t高中");
        //\n:制表位，换行符
        System.out.println("小学\n初中\n高中");
        // \\:一个\
        System.out.println("D:\\Study\\se");
        // \":一个"
        System.out.println("我说:\"xiexieni\"");
        // \':一个'
        System.out.println("我说:\'xiexieni\'");
        // \r:一个回车
        System.out.println("abcdef\r\ngh");
        //exercise1
        System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t120\t1000");
    }
}
