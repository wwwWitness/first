public class Char01 {
    public static void main(String[] args) {
        char a1 = 'a';
        char a2 = '\t';
        char a3 = '王';
        char a4 = 100;//说明 字符能直接存放数字
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
        System.out.println(a4);//100对应的字符
    }
}
